// Operadores Aritméticos em JavaScript
// Adição: + (soma)    
// Subtração: - (subtração)
// Multiplicação: * (multiplicação)
// Divisão: / (divisão)
// Módulo: % (resto da divisão)

const num1 = 5;
console.log(num1 + 2);
console.log(num1 - 2);
console.log(num1 * 2);
console.log(num1 / 2);
console.log(num1 % 2);

// Potenciação
console.log(5**2);

// Incremento
let num2 = 5;
console.log(num2++);
console.log(num2);
console.log(++num2);
console.log(num2);

// Decremento
let num3 = 5;
console.log(num3--);
console.log(num3);
console.log(--num3);
console.log(num3);