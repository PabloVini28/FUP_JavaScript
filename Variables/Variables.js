var Nome = 'Pablo';
// Não é boa praticar usar var, pois ela é global e pode ser redeclarada

let Nome1 = 'Pablo';

// let é uma variável local, ou seja, ela só pode ser usada dentro do escopo onde foi declarada
const numero = 5;

// const é uma constante, ou seja, ela não pode ser alterada

console.log(Nome);
console.log(Nome1);
console.log(numero);